package com.Bridgelabz.practice;

public class OperatorsType {

	public static void main(String[] args) {
		int x=10;
		int a=10;
		int b=5;
		System.out.println(10*10/5+3-1*4/2);
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		System.out.println(x++);
		System.out.println(++x);
		System.out.println(x--);
		System.out.println(--x);

		
	}

}
